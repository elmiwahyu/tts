const green = "#AAD59E";
const red = "#F0743E";

document.addEventListener("DOMContentLoaded", function () {
  const kolomW6L1 = document.querySelector("#W6L1");

  kolomW6L1.addEventListener("input", (event) => {
    let nilaiInput = event.target.value.toUpperCase();
    if (nilaiInput === "L") {
      kolomW6L1.style.backgroundColor = green;
    } else {
      kolomW6L1.style.backgroundColor = red;
    }
  });
});

